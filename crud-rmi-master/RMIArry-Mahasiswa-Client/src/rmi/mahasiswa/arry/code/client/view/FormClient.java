    //Prosedur ketika tombol insert ditekan
    private void buttonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInsertActionPerformed
        try {
            //Membuat objek mahasiswa dari package API
            Mahasiswa mahasiswa = new Mahasiswa();
           
            //Menyimpan record data dari client ke tiap tiap entitas
            mahasiswa.setNpm(txtNpm.getText());
            mahasiswa.setNama(txtNama.getText());
            mahasiswa.setJurusan((String) comboJurusan.getSelectedItem());
            mahasiswa.setAlamat(txtAlamat.getText());
            mahasiswa.setPhone(txtPhone.getText());
            
            //mengirim perimntaan kepada server untuk prosedur INSERT
            Mahasiswa mahasiswa1 = service.insertMahasiswa(mahasiswa);
            
            //menampilkan feedback yang diberikan oleh server
            JOptionPane.showMessageDialog(null, mahasiswa1.getLaporan());
            refresh(); //merefresh table
            
         } catch (RemoteException ex){
            ex.printStackTrace();   //feedback ketika tidak bisa menghubungi server
         }
    }//GEN-LAST:event_buttonInsertActionPerformed

