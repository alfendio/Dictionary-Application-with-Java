    //prosedur untuk merefresh table pada JFrame Client
    public void refresh(){
        txtNama.setText("");
        txtNpm.setText("");
        txtAlamat.setText("");
        txtPhone.setText(""); 
       
        try {
            int row = tableMahasiswa.getSelectedRow();
            if (row == -1) {
                return;
            }
            List<Mahasiswa> list = service.getAllMahasiswa();  
            tableModelMahasiswa.setData(list);
         } catch (RemoteException e) {  
            e.printStackTrace();
         }
    }
    
    
