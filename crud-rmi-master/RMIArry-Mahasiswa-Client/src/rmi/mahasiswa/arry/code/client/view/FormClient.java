    //Prosedur cari untuk mencari data yang ada pada table JFrame
    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed
        TableRowSorter rowSorter = new TableRowSorter(tableModelMahasiswa);
        txtSearch.getDocument().addDocumentListener(new DocumentListener() {

            public void insertUpdate(DocumentEvent e) {
                tableMahasiswa.setRowSorter(rowSorter);
                rowSorter.setRowFilter(RowFilter.regexFilter(txtSearch.getText()));
            }

            public void removeUpdate(DocumentEvent e) {
                tableMahasiswa.setRowSorter(rowSorter);
                rowSorter.setRowFilter(RowFilter.regexFilter(txtSearch.getText()));
            }

            public void changedUpdate(DocumentEvent e) {
                tableMahasiswa.setRowSorter(rowSorter);
                rowSorter.setRowFilter(RowFilter.regexFilter(txtSearch.getText()));
            }
        });
    }//GEN-LAST:event_buttonSearchActionPerformed

